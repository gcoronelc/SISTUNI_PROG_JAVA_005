    private void mostrar (ItemDto[] reporte) {
        //Acceder al TableModel
        DefaultTableModel tabla;
        tabla = (DefaultTableModel)tblTabla.getModel();
        
        //Borrar la data
        tabla.setRowCount(0);
        
        //Agregar la nueva data
        for(ItemDto dto: reporte) {
            Object[] rowData = {dto.getConcepto(), dto.getValor()};
            tabla.addRow(rowData);
        }
        
    }
