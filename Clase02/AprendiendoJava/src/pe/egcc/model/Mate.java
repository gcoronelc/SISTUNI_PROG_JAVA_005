public class Mate {

  public int sumar(int n1, int n2){
    //System.out.println("Sumar enteros");
    return (n1 + n2);
  }
  
  public int sumar(int n1, int n2, int n3){
    return (n1 + n2 + n3);
  }
  
  public double sumar(double n1, double n2){
    //System.out.println("Sumar double");
    return (n1 + n2);
  }
  
  
  
  
}
